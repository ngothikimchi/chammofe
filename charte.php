<div class="sous-titre">Règlement du site :</div>
<div class='blockcharte'>Les associations et entreprises mises en relation par le site s’engagent à accepter 
        les conditions suivantes :</br>
-	Il est interdit d’utiliser ChAmMoFe a des fins publicitaires</br>
-	Toutes les marchandises échangées le sont à titre gracieux. Tout échange de devise, de bien ou de service concernant
 les biens échangés sur ChAmMoFe est strictement interdit, que ce soit avant, après ou pendant l’échange</br>
-	Les marchandises échangées doivent respecter les normes européennes</br>
-	Pas de favoritisme entre les associations et les entreprises : Une demande doit répondre à une offre en 
fonction de son contenu et non de son émetteur.</br>
-	La qualité des produits (type, marque,contenu) annoncé doit être respectée.
</div>

<div class="titremention">Les offres et les demandes :</div>
<div class="titremention">Offre :</div> 
<div class="blockcharte">
        
-	Les offres concernant des produits périssables ne doivent pas dépasser la date de limite de consommation du 
produit le plus vieux du lot.</br>
-	On ne peut plus modifier une offre après qu’elle a été acceptée, même partiellement.</br>
-	Une offre concerne un seul produit.

</div>

<div class="titremention">Demande :</div>

<div class="blockcharte" >
-	Une demande ne répond qu’à une offre et ne concerne qu’un seul produit
</div>


