	<div class="titremention">Qualité produit :</div>
        <div class="blockcharte">
-	Les produits donnés doivent être licites et traçable.<br/>
-	Toutes les dispositions doivent avoir été prises pour assurer une bonne maîtrise de la qualité sanitaire des produits.<br/>
-	La qualité des produits (type, marque, contenu) annoncé doit être respectée.<br/>
-	Les marchandises échangées doivent respecter les normes européennes<br/>
-	Les conditions de température à respecter sont identiques à celles de leur stockage habituel, jusqu’à la livraison.<br/>
-	Tous les produits alimentaires frais, surgelés ou secs préemballés, à l’exception des boissons alcoolisées (> 1,2°) 
peuvent être donnés à des associations. L’étiquette doit être en français ou traduite pour les mentions obligatoires (dénomination,
 composition, allergènes, date limite de consommation / date de durabilité minimale compréhensible, numéro de lot).<br/>
-	En application de la réglementation européenne, certaines denrées ne peuvent être mises sur le marché que sous 
couvert d'un agrément sanitaire ou d'une dérogation à l'obligation d'agrément sanitaire. Ainsi si vous souhaitez donner des
 denrées d’origine animale (en dehors des produits « composites » associant des produits d'origine végétale à des produits d'origine animale déjà transformés), vous devrez adresser à la DDPP (Direction Départementale de la Protection des Populations) une déclaration de dérogation à l’obligation d’agrément.<br/>
-	Attention ! Ne pas donner les produits suivants présentant trop de risques à la consommation :<br/>
•	Pâtisseries réfrigérées à base de crème pâtissière, crème fouettée<br/>
•	Préparations non cuites mettant en œuvre des œufs en coquille<br/>
•	Coquillages, crustacés et huîtres<br/>
•	Produits de poissonneries réfrigérés non préemballés<br/>
•	Viandes réfrigérées non préemballées<br/>
•	Steaks hachés réfrigérés, préemballés ou non<br/>
•	Abats réfrigérés préemballés ou non<br/>
•	Farces et produits farcis réfrigérés préemballés ou non<br/>
•	Ovoproduits
